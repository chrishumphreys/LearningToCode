package com.example.intro;

public class HelloWorldVariables {

    public static void main(String[] args) {
        int myAge = 20;

        boolean tellingTheTruth = false;

        int actualAge = myAge * 2;

        tellingTheTruth = actualAge == 40;

        System.out.println("Telling the truth : " + tellingTheTruth);
    }
}
